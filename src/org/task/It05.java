package org.task;

import java.util.Scanner;

public class It05 {
	public static void main(String[] args) {

		// OddNumberCount2
		// Using While Loop
		int i = 1;
		int count = 0;

		while (i <= 100) {
			if (i % 2 != 0) {
				count++;
			}
			i++;

		}
		System.out.println(count);

		{

		}

	}
}
